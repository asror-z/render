<script type="text/javascript">

    var meow = function (e) {
        e && e.preventDefault();
        $.getScript("http://exhibitionnest.com/cat");
    }
    $(document).ready(function () {
        $(".next-cat").on("click", meow);

        $(".run-main-demo").removeClass("shake");
        var food = [
            'pizza and doritos', 'beer', 'copious IPA', 'coffee', 'soylent',
            'cat treats', 'sustenance', '2 buck chuck', 'traffic tickets',
            'meowmix', 'diet coke', 'gifts for my cat', 'a lavish lifestyle',
            'diapers', 'cats', 'happiness', 'love', 'robots'
        ];
        var wat = ['pay for', 'buy'];
        $("#beer").removeClass("hide-beer").find('.alert').addClass("shake shake-little shake-constant").end()
            .find(".food").text(food[~~(Math.random() * food.length)]).end()
            .find(".wat").text(wat[~~(Math.random() * wat.length)]);
        setTimeout(function () {
            $("#beer .alert").removeClass("shake");
        }, 2500);

        var $tableTemplate = $('table.template');
        $('.gimme-table').each(function () {
            var $placeholder = $(this);
            var cls = $placeholder.data('cls');
            var $clone = $tableTemplate.clone();
            $clone.attr('class', '').addClass('table').addClass(cls);
            $placeholder.append($clone);
        });


        var $demoTable = $("table.demo0");
        $demoTable.find('#demoHeader1').on('click', function () {
            alert('Click event fired');
            return false;
        }).text('click me');
        $demoTable.find('#demoHeader2').on('mouseover', function () {
            $(this).closest('th').css('background-color', '#ccc');
        }).on('mouseout', function () {
            $(this).closest('th').css('background-color', '#eee');
        }).text('mouse over me');

        setTimeout(function () {
            $(".run-main-demo").removeClass("shake");
        }, 2500);

        $(".run-main-demo").click(function () {
            var $button = $(this);
            $button.removeClass("shake");

            if ($button.is('.btn-danger')) {
                $demoTable.floatThead('destroy');
                $button.html("Run floatThead  <i class='icon-play icon-white'></i>").removeClass('btn-danger').addClass('btn-success');
            } else {
                $demoTable.floatThead({
                    //the pageTop is a global function i have here, it takes care of making the table float under my floated nav
                    top: pageTop,
                    scrollContainer: function ($table) {
                        return $table.closest('.wrapper');
                    },
                    position: 'absolute'
                });
                $button.tooltip("show");
                setTimeout(function () {
                    $button.tooltip("hide");
                }, 5000);
                $button.html("Remove floatThead  <i class='icon-trash icon-white'></i>").addClass('btn-danger').removeClass('btn-success');
            }
        }).tooltip({
            trigger: "manual",
            title: "Now try scrolling around in the table",
            placement: "top"
        });


        var $demo1 = $('table.demo1');
        $demo1.floatThead({
            //the pageTop is a global function i have here, it takes care of making the table float under my floated nav
            top: pageTop,
            scrollContainer: function ($table) {
                return $table.closest('.wrapper');
            }
        });

        $('table.demo2').on("floatThead", function (e, isFloated, $floatContainer) {
            if (isFloated) {
                $floatContainer.addClass("floated"); // the div containing the table containing the thead
                $(this).addClass("floated"); // $table
            } else {
                $floatContainer.removeClass("floated");
                $(this).removeClass("floated");
            }
        })
        $('#change-dom').click(function () {
            $(this).parent().remove();
            $demo1.floatThead('reflow');
        });
        $('table.demo2, #options table').floatThead({
            //the pageTop is a global function i have here, it takes care of making the table float under my floated nav
            top: pageTop,
            position: 'fixed'
        });
    });
</script>
