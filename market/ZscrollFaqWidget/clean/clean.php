    $('.js-sticky-nav').stick_in_parent({
        offset_top: 111
    })
        .on('sticky_kit:bottom', function (event) {
            $(this).parent().css('position', 'static');
        })
        .on('sticky_kit:unbottom', function (event) {
            $(this).parent().css('position', 'relative');
        });


    var sections = $('.js-scroll-section')
        , nav = $('nav')
        , nav_height = nav.outerHeight();

    $(window).on('scroll', function () {
        var cur_pos = $(this).scrollTop();

        sections.each(function() {
            var top = $(this).offset().top - 390,
                bottom = top + $(this).outerHeight();

            if (cur_pos >= top && cur_pos <= bottom) {
                nav.find('a').removeClass('active');
                sections.removeClass('active');

                $(this).addClass('active');
                nav.find('a[href="#'+$(this).attr('id')+'"]').addClass('active');
            }
        });
    });

    nav.find('a').on('click', function () {
        var $el = $(this)
            , id = $el.attr('href');

        $('html, body').animate({
            scrollTop: $(id).offset().top - 320
        }, 500);

        return false;
    });




    $(function () {
        if (!window.localStorage.getItem('language_chosen')) {
            $('<div/>').css({
                position: 'fixed',
                top: 0,
                left: 0,
                right: 0,
                bottom: 0,
                backgroundColor: 'black',
                opacity: 0.8,
                zIndex: 3000,
                display: 'none'
            }).appendTo($('body')).click(function () {
                $(this).fadeOut(400, function () {
                    $(this).remove()
                })
                $('li.toolbar__item--lang').find('div.dropdowns').removeClass('dropdowns-opened').css('z-index', 'inherit')
            }).fadeIn(600, function () {
                $('li.toolbar__item--lang').find('div.dropdowns').addClass('dropdowns-opened').css('z-index', 3001)
            })
            window.localStorage.setItem('language_chosen', '1')
        }

        var hide_top_header_banner = $.cookie('hide_top_header_banner');
        if(hide_top_header_banner != 1) {
            $('.headerBanner').attr('style', 'display: block !important;');
            $('.q-header_xmas').attr('style', 'background-position-y: 50%');
        }else{
            $('.headerBanner').remove();
            $('.q-header_xmas').attr('style', 'background-position-y: 0%');
        }
        $('.close_banner').click(function(){
            $('.headerBanner').remove();
            $('.q-header_xmas').attr('style', 'background-position-y: 0%');
            $.cookie('hide_top_header_banner', 1, {expires : 1, path : '/'});
        });

    });


    var modal_tg = $.cookie('modal_tg');

    setTimeout(function () {

        if(modal_tg != 1) {
            $("#modal_tg").show(function () {
                $('#modal_tg').find('.modal__content').addClass('modal__content--in');
            });
            $('#modal_tg').on('shown.bs.modal', function () {
                $('#modal_tg').find('.modal__content').addClass('modal__content--in');
            });
        }
        $("#modal_tg .js-purchase-modal-close, #modal_tg a.tg").click( function (){
            $('#modal_tg').find('.modal__content').removeClass('modal__content--in');
            $("#modal_tg").hide();
            $.cookie('modal_tg', 1, {expires : 21, path : '/'});
        });

    }, 5000);

</script>

