                    items: 4,
                },
                768: {
                    items: 4,
                },
                1170: {
                    items: 3,
                },
                1200: {
                    items: 4,
                }
            },
            rtl: false,
        });

        // Init the zoom

        if ($.isFunction($.fn.elevateZoom)) {
            $(".wpb-wiz-main-image").elevateZoom({
                gallery: 'wpb_wiz_gallery',
                galleryActiveClass: 'wpb-wiz-active',
                imageCrossfade: true,
                loadingIcon: 'http://demo2.wpbean.com/wp-content/uploads/2016/07/AjaxLoader.gif',
                cursor: "crosshair",
                lensSize: 200,
                lensShape: "square",
                lensColour: "#ffffff",
                borderSize: 1,
                borderColour: '#888888',
                zoomType: "inner",
                responsive: true,
                zoomWindowWidth: 400,
                zoomWindowHeight: 400,
                zoomWindowFadeIn: 500,
                zoomWindowFadeOut: 500,
                zoomWindowPosition: 1,
                zoomWindowOffetx: 10,
                easing: true,
            });
        }

        // Popup With zoom
        $(".wpb-wiz-main-image").bind("click", function (e) {
            var ez = $('.wpb-wiz-main-image').data('elevateZoom');


            ez.closeAll(); //NEW: This function force hides the lens, tint and window
            $.fancybox.close();
            $.fancybox.open(
                ez.getGalleryListFancyboxThree(), {});


            return false;
        });

        // Remove srcset & size attr
        $("#wpb_wiz_gallery a").on("click", function () {
            $('.single-product .wpb-wiz-main-images img').removeAttr('srcset');
            $('.single-product .wpb-wiz-main-images img').removeAttr('sizes');
        });

    });

